// 没手动选过语言时跟随系统语言
function sysLang() {
  try {
    const l = (navigator.language || 'zh').toLowerCase();
    if (l.startsWith('zh')) return 'zh';
    if (l.startsWith('ja')) return 'ja';
    return 'en';
  } catch (_) { return 'zh'; }
}

export function getLang() {
  try { return localStorage.getItem('lang') || sysLang(); } catch (_) { return 'zh'; }
}

export function setLang(lang) {
  try {
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang; // CSS 按语言切字体栈/排版
  } catch (_) {}
}

// 模块加载即打上 lang 标记，CSS 的 html[lang=...] 规则立即生效
try { document.documentElement.lang = getLang(); } catch (_) {}

export function t(zh, vars) {
  const lang = getLang();
  let s = zh;
  if (lang !== 'zh') {
    const dict = DICTS[lang];
    s = (dict && dict[zh]) || zh;
  }
  if (vars) {
    for (const k of Object.keys(vars)) s = s.split(`{${k}}`).join(String(vars[k]));
  }
  return s;
}

